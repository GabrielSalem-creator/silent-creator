def build_prompts(universe_id: str, action_desc: str, static_desc: str,
                  location_override: str = None,
                  narrative_context: str = "",
                  time_of_day: str = "") -> tuple[str, str]:
    c = CHARACTERS[universe_id]
    # Always frame the environment as an anime illustration — never let a raw
    # realistic location string override the visual style.
    if location_override:
        env = f"anime illustrated setting: {location_override}. Soft cel-shaded lighting, hand-drawn textures."
    else:
        env = c["env_seed"]

    context_prefix = ""
    if time_of_day:
        context_prefix += f"Time: {time_of_day}. "
    if narrative_context:
        context_prefix += f"Mood: {narrative_context} "

    base = (
        "2D lofi anime illustration frame. Studio Ghibli aesthetic. 90s retro cel-shaded animation. "
        "Hand-drawn line art with ink outlines. Painted anime background. Soft pastel color palette. Moody ambient lighting. "
        "Classic anime proportions and facial styling. Lo-fi cozy atmosphere. "
        "NOT photorealistic. NOT realistic skin texture. NOT live action. NOT CGI. NOT 3D render. NOT DSLR photo. Illustrated anime only. "
        f"{env} "
        f"Illustrated anime character — {c['seed']} "
        f"{context_prefix}"
    )
    action = (
        base +
        f"ACTION: {c['name']} {action_desc} "
        "4-second animated clip. Anime illustration style throughout. No text, no subtitles, no watermarks."
    )
    static = (
        base +
        f"IDLE LOOP — exact same anime scene and location as above, moment after the action. "
        f"{c['name']} {static_desc} "
        "Character is now still. Same lighting, same objects, same environment. "
        "Only micro-movements: slow breathing, a single blink, one ambient element shifting. "
        "Seamless loop. Anime illustration style. No text, no subtitles, no watermarks."
    )
    return action, static


def _start(universe_id, title, action_desc, static_desc, choices):
    ap, sp = build_prompts(universe_id, action_desc, static_desc)
    return {
        "universeId": universe_id,
        "title": title,
        "depth": 1,
        "timeOfDay": "",
        "location": "",
        "actionPrompt": ap,
        "staticPrompt": sp,
        "narrativeContext": "",
        "choices": [
            {**ch, **SYMBOL_META.get(ch["symbol"], {"color":"#fff","rgb":"255,255,255"}),
             "nextNode": None}
            for ch in choices
        ],
    }


